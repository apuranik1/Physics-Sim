package racing.game;
/**
 * Specification of Cart
 */
public class Spec {
	public int weight;
	public int acceleration;
	public int topspeed;
	public int handling;
	public Spec(int weight, int acceleration, int topspeed, int handling){
		this.weight=weight;
		this.acceleration=acceleration;
		this.topspeed=topspeed;
		this.handling=handling;
	}
}
